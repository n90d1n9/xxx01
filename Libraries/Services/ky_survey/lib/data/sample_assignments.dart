SurveyAssignment _assignment({
  required String id,
  required String surveyId,
  required String assigneeId,
  required String assigneeName,
  required String territory,
  required SurveyAssignmentStatus status,
  required int targetResponses,
  required int completedResponses,
  required int dueInDays,
  String? note,
}) {
  return SurveyAssignment(
    id: id,
    surveyId: surveyId,
    assigneeId: assigneeId,
    assigneeName: assigneeName,
    territory: territory,
    status: status,
    targetResponses: targetResponses,
    completedResponses: completedResponses,
    dueAt: DateTime.now().add(Duration(days: dueInDays)),
    assignedAt: DateTime.now().subtract(const Duration(days: 6)),
    note: note,
  );
}
