class Vehicle {
  final String id;
  final String make;
  final String model;
  final String year;
  final String licensePlate;
  final int mileage;
  final String vinNumber;

  Vehicle({
    required this.id,
    required this.make,
    required this.model,
    required this.year,
    required this.licensePlate,
    required this.mileage,
    required this.vinNumber,
  });
}
